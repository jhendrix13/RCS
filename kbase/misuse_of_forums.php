<h1>Rule 14 - Misuse of Official Forums</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You must not misuse our Official <?php echo $data['wb_name']; ?> Forums. The forum code of conduct (linked at the top of the forums) clearly details exactly what is and what is not acceptable behaviour.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
This rule is in place to maintain a fun and friendly atmosphere within the forums. The main purpose of the forums is for players to help each other. This could be by chatting or trading, but always in a safe and secure environment.<br>
<br>
<h4 style="display: inline;">
What is not tolerated in the <?php echo $data['wb_name']; ?> Forums?
</h4><br>
<br>
The <?php echo $data['wb_name']; ?> <a href="http://forum.<?php echo $data['wb_name']; ?>.com/codeofconduct.ws">Forum Code of Conduct</a> details exactly what is and what is not acceptable behaviour within the <?php echo $data['wb_name']; ?> forums. There are also stickies at the top of each forum with explanations of aspects of the <?php echo $data['wb_name']; ?> Forum rules.<br>
<br>
<h4 style="display: inline;">
Can I promote my fan/clan site in-game/in the forums?
</h4><br>
<br>
It is a breach of the rules to post anything promoting a website in the forums or in the game. As such, if you do post the URL for your own clan site, you could be banned.<br>
<br>
We understand this may cause some inconvenience, but please remember that this rule is there to make it impossible for people to send players to scam sites.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
