<h1>Rule 5 - <?php echo $data['wb_name']; ?> Staff Impersonation</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You should not attempt to impersonate <?php echo $data['wb_name']; ?> staff in any way, for any reason.<br>
<br>
The names of <?php echo $data['wb_name']; ?> Staff will have a gold crown next to their name when speaking in-game. Anyone who does not have a gold crown next to their name is not <?php echo $data['wb_name']; ?> staff. This applies to both private chat and general in game chat.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule in place so our players can identify members of <?php echo $data['wb_name']; ?> staff and to ensure that players can trust members of our staff for help and support with any game related problems they may have.<br>
<br>
<h4 style="display: inline;">
Someone claiming to work for <?php echo $data['wb_name']; ?> has asked me for my account details. Should I give it to them?
</h4><br>
<br>
There are three ways to recognise <?php echo $data['wb_name']; ?> Staff:<br>
<ul style="margin-top: 0px; margin-bottom: 0px; list-style-type: decimal;">
<li style="list-style: none">
<br>
</li>
<li><?php echo $data['wb_name']; ?> Staff characters always have gold crowns next to their names<br>
</li>
<li><?php echo $data['wb_name']; ?> Staff will never ask for a password.<br>
</li>
<li><?php echo $data['wb_name']; ?> Staff will never contact you about your account in game.<br>
</li>
</ul><br>
We have no 'undercover' staff, we have no need for your password to check your account, and we only communicate with players about account issues via their message center.<br>
<br>
If you ever see anyone attempting to impersonate a <?php echo $data['wb_name']; ?> Staff member or any other player then we would ask you to use the 'Report Abuse' button located at the bottom of the screen, and follow the on screen prompts so that we can take the appropriate actions against their account.<br>
<br>
<h4 style="display: inline;">
If <?php echo $data['wb_name']; ?> Staff have gold crowns, who are the players in-game that have silver crowns?
</h4><br>
<br>
The players that have silver crowns by their names when speaking in-game are Player Moderators. Anyone that does not have a silver crown next to their name is not a Player Moderator.<br>
<br>
For more information on Player Moderators please see the <a href="../Customer_Support/Moderators/Player_Moderators/category.html">Player Moderator</a> section in Customer Support.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
