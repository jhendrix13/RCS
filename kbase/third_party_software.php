<h1>Rule 7 - Using Third Party Software</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 7th March 2007</span><br>
<br>
You must not use other software to gain an unfair advantage in the game. This includes automation tools, macros, bots, or auto-typers. You may not circumvent any of our mechanisms designed to automatically log out inactive users.<br>
<br>
You also must not use any <?php echo $data['wb_name']; ?>-specific third party software which encourages breaking of our rules, or bypasses the normal navigation of our website, or automatically requests pages from our website, or which endangers user accounts.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
First, we don't want players to download third party <?php echo $data['wb_name']; ?> software from untrusted sources that can, and often do, contain keyloggers, trojans and viruses. Many players have had their accounts stolen because they have inadvertently installed hacking software on their machines. Even if the author has good intentions, an unintended flaw in their software could still put your account at risk. Even some supposedly reputable software has had problems in the past. There are also cases of people deliberately writing software which looks like a cool <?php echo $data['wb_name']; ?> add-on but actually just steals your password.<br>
<br>
Secondly, macroing ruins the game for everyone else. We don't want some players having an unfair advantage and filling up all the best training spots with bots. This game is designed to be enjoyed by humans, not bots. In the past, when we have not enforced this rule, we have rapidly found it quickly spoils the game, and players themselves start demanding that we enforce it. So now we enforce it thoroughly.<br>
<br>
<h4 style="display: inline;">
What type of third party software is NOT allowed?
</h4><br>
<br>
This includes, but is not limited to the following third party software:<br>
<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li style="list-style: none">
<br>
</li>
<li>Software that attempts to communicate directly with the <?php echo $data['wb_name']; ?> game worlds.<br>
</li>
<li>Software that monitors, modifies or decrypts the communications between the <?php echo $data['wb_name']; ?> applet and the <?php echo $data['wb_name']; ?> worlds.<br>
</li>
<li>Software that generates input to the <?php echo $data['wb_name']; ?> applet. This includes software that automatically moves the mouse pointer or generates mouse clicks or key presses.<br>
</li>
<li>Modified or replacement versions of the <?php echo $data['wb_name']; ?> applet. Reverse engineering the <?php echo $data['wb_name']; ?> applet is strictly prohibited. Using a modified version of the <?php echo $data['wb_name']; ?> applet is not permitted for any reason.
</li>
</ul><br>
<br>
Third party software that displays pages/content from our website(s), and which has <?php echo $data['wb_name']; ?> specific features (i.e. <?php echo $data['wb_name']; ?> toolkits) must not be used to access our website <b>unless</b> that software conforms with all of the other rules, and the following additional rules:<br>
<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li style="list-style: none">
<br>
</li>
<li>It must not encourage rule-breaking or endanger users.<br>
</li>
<li>It must not contain adverts or links to websites which encourage rule-breaking or endanger users.<br>
</li>
<li>It must not automatically make repeated page/content requests from our website or otherwise make excessively bandwidth intensive requests from our servers (each page/content request should only be in direct response to a user request each time).<br>
</li>
<li>It must not bypass the normal navigation of our website for users wishing to reach the game worlds. The software should initially only point at the front page of www.<?php echo $data['wb_name']; ?>.com, and should require the user to navigate through the website normally to reach the game worlds. The software must not contain or acquire direct links to our "Select a world" page, or to our game world pages, or to the <?php echo $data['wb_name']; ?> applet.<br>
</li>
<li>It must not hide or encourage the hiding of any of the adverts on our website. To avoid encouraging users to hide the advert above the game the pane displaying pages from our website must default to at least 630 pixels high (if viewed on a screen with a resolution higher than 800x600), and it must not be possible to resize it to less height than that. i.e. the page must be displayed in such a way that both the game and advert are fully visible at the same time.<br>
</li>
<li>It must not attempt to make it easier to enter a busy <?php echo $data['wb_name']; ?> world than it would be for users not using the software. It must not allow worlds marked as 'FULL' on the world select page to be selected. It must not contain any features to make it easier to enter or monitor busy worlds.<br>
</li>
<li>If the software has its own integrated chat feature, separate from the <?php echo $data['wb_name']; ?> chat, it should make it very clear to users that this chat is not part of <?php echo $data['wb_name']; ?> and is not official, endorsed or moderated by Jagex in any way. Furthermore, this chat should still be moderated (just not by us), and the moderators should not allow chat which infringes our rules of conduct. To avoid confusion with the official <?php echo $data['wb_name']; ?> chat, moderators of the external chat should not be indicated with a silver or gold crown. If the chat is IRC based, then the software should only allow access to IRC channels which conform with the above.
</li>
</ul><br>
<br>
In any case, the use of such software is not recommended. We do not recommend the use of ANY 3rd party <?php echo $data['wb_name']; ?>-related software as it is potentially extremely dangerous. Please be aware that there is software out there which claims to be a cool <?php echo $data['wb_name']; ?> add-on, but actually installs a keylogger on your computer and steals your password! You should also be aware that there is dangerous software around that won't necessarily be spotted by your anti-virus software. Scanning downloads is, of course, a very good idea, but it still doesn't 100% eliminate the risk.<br>
<br>
<h4 style="display: inline;">
What do you mean by "software"?
</h4><br>
<br>
Software means any type of software, including: downloadable software (e.g an .exe file), or web-based software (e.g a website set up to perform a similar function with flash, java, javascript or serverside scripts).<br>
<br>
<h4 style="display: inline;">
How does the new macro detection system work?
</h4><br>
<br>
Our new macro detection system monitors activity on our servers and detects suspicious behaviour. This evidence is reviewed by our team of experts who either confirm or reject the system's analysis. Jagex will not reveal the exact mechanics of how this system works because this would make circumvention easier.<br>
<br>
<h4 style="display: inline;">
Will I be able to appeal against a ban for macroing?
</h4><br>
<br>
Our policy has always been that we do not tolerate any form of cheating. We do not ban accounts lightly. Significant evidence is always collected before any action is taken against a player. We will not reveal the evidence that we have collected, since most cheats only want to know the evidence so that they can avoid getting caught the next time.<br>
<br>
<h4 style="display: inline;">
What is AFK Training?
</h4><br>
<br>
AFK training is Away From Keyboard training. While you are playing on <?php echo $data['wb_name']; ?>, you must be at the computer. Away from keyboard training is not allowed, and you should log out when you leave the computer.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
