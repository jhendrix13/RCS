<h1>Rule 1 - Offensive Language</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 2nd May 2007</span><br>
<br>
You must not use any language which may be considered offensive, racist, obscene or otherwise inappropriate. This includes swearing.<br>
<br>
If you suffer serious abuse such as receiving racist remarks, harassment or obscene and vulgar language, please report them to us using the in-game <b>Report Abuse</b> button which can be found at the bottom of the game window.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule to maintain a pleasant environment in the game. For many people it's not much fun playing if there is someone else being rude or offensive, so please consider others when chatting. Also please remember that not everyone playing is the same age or from the same background, so may be more offended or upset than you expect.<br>
<br>
<h4 style="display: inline;">
What do you mean by offensive or inappropriate language?
</h4><br><br>This includes (but is not limited to): discussion of recreational drugs, sexually explicit language, solicitation, racism or other prejudice, threatening, blackmail and swearing. It's simply a matter of common sense - try not to say anything that someone might find offensive.<br>
<br>
<h4 style="display: inline;">
Is it okay to ask for a boyfriend or girlfriend in <?php echo $data['wb_name']; ?>?
</h4><br><br>No, that is solicitation and is not allowed at all. This is to ensure the safety of all our players.<br>
<br>
<h4 style="display: inline;">
What about mis-spelling an offensive word to disguise it? Is that okay?
</h4><br>
<br>
No, please don't do this. In fact, mis-spelling an offensive word or using symbols to replace letters to avoid the chat filter is considered even worse.<br>
<br>
<h4 style="display: inline;">
I was called a 'noob'. Is this offensive? Should I report this player?
</h4><br>
<br>
Calling other players a 'noob' is quite common in most online games.<br>
<br>
It's not something to worry about as it means 'new player'. Every player has to be a 'noob' at some point.<br>
<br>
The best thing to do is to ignore it.<br>
<br>
<h4 style="display: inline;">
What is flooding the chat window?
</h4><br>
<br>
Spamming/flooding the chat window is to fill the chat window with unnecessary text. This is also an offence and is disruptive and unfair on other players as it makes it more difficult for them to communicate and trade with each other.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
