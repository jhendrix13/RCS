<h1>Rule 3 - Password Scamming</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
Asking/trying to persuade another player to give you their password for any reason is against the rules and will not be tolerated.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
This rule is in place to protect our players' accounts. Even if you genuinely only wanted to 'borrow' their account we obviously can't tell the difference between this and a malicious person stealing accounts. So asking other players for their password will be treated VERY seriously.<br>
<br>
<h4 style="display: inline;">
What if I try to get someone else's password but don't succeed? Have I still broken the rule?
</h4><br>
<br>
Yes, you have still broken the rule - even if you aren't successful this will almost certainly result in a permanent ban being placed on your account. You shouldn't even be trying to get someone else's password!<br>
<br>
<h4 style="display: inline;">
What if I ask in a different language?
</h4><br>
<br>
The rules apply equally to all languages, therefore you will still be breaking the game rules.<br>
<br>
<h4 style="display: inline;">
I have submitted a Password Request because I have forgotten my password. Why has it not been actioned yet?
</h4><br>
<br>
You must submit sufficient accurate information to Customer Support in order for us to issue you with your password again. If we feel the information that you have supplied is not adequate for us to consider you the original owner of the account, then we will not give you control of that account.<br>
<br>
Please do not send in multiple password requests with inaccurate information to Customer Support, unless you want to present new additional and highly compelling evidence that you are the owner. If you do so this may be deemed a misuse of Customer Support, which may then be withdrawn from your account.<br>
<br>
<h4 style="display: inline;">
Someone has asked me for my bank PIN. Is it all right if I tell them?
</h4><br>
<br>
Asking for someone's bank PIN is just as serious as asking for an account password. It allows other players access to your account. Under no circumstances should you tell another player your password, PIN number or any other personal details of your account.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
