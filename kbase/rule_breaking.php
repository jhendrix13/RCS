<h1>Rule 9 - Encouraging Others to Break Rules</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You must not encourage others to break any of the <?php echo $data['wb_name']; ?> rules.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
The reason for this rule is pretty obvious. Encouraging or helping others to break the rules is as bad as breaking them yourself, and therefore anyone doing so will be treated as if they were the one breaking the rule.<br>
<br>
<h4 style="display: inline;">
What do you mean by encouraging others to break the rules?
</h4><br>
<br>
Examples of encouraging others to break any of the <?php echo $data['wb_name']; ?> rules would include:<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li style="list-style: none">
<br>
</li>
<li>Attempting to convince other players that the rules of <?php echo $data['wb_name']; ?> are different from what they actually are.<br>
</li>
<li>Trying to get a reaction from another player by using offensive language against them.<br>
</li>
<li>Creating, distributing or advertising third party <?php echo $data['wb_name']; ?> add-ons which break <a href="http://www.<?php echo $data['wb_name']; ?>.com/Rules_of_Conduct/Rule_7_Using_Third_Party_Software.wsi">Rule 7</a>.<br>
</li>
<li>Advertising or linking to real-world trading websites which break <a href="http://www.<?php echo $data['wb_name']; ?>.com/Rules_of_Conduct/Rule_12_Real_World_Item_Trading.wsi">Rule 12</a>.<br>
</li>
<li>Encouraging others to abuse bugs - any bugs found should be reported to Jagex immediately.<br>
</li>
<li>If you run a fansite linking to or providing software which links to forums/chat where users discuss ways of breaking rules, or encourage each other to do so (unless a genuine effort is made to moderate such chat and prevent such discussion).<br>
</li>
<li>... there are many more examples; basically, anything that makes someone else more likely to break a rule.<br>
</li>
</ul><br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
