<h1>Rule 15 - Advert Blocking</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You must not deliberately block or hide the adverts which appear above the free version of our game.<br>
<br>
If you have advert blocking software please prevent it from blocking adverts on our website. The adverts above the free game are what enable us to keep it free, after all!<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
The adverts above the free-to-play version of <?php echo $data['wb_name']; ?> are what enable us to keep the free game running! The sponsors who show their adverts there are the ones paying for you to enjoy a free game in the first place, so please don't block them.<br>
<br>
<h4 style="display: inline;">
What if I find an advert to be offensive?
</h4><br>
<br>
If you believe there is an offensive advert, please click on the Question-Mark "Icon" shown to the bottom right of the advert. This will pop up a window with instructions on how to report that advert.<br>
<br>
Once we have this report, we can then look into the advert that is offending you, the last thing we want is for any of the advertising that we are supplied with to offend to our players.<br>
<br>
<h4 style="display: inline;">
What if adverts slow my PC down?
</h4><br>
<br>
Certain animated adverts may cause your PC to slow down while they are displayed. This is because your computer is busy running the advert animation which gives it less time to run the game.<br>
<br>
Most adverts don't cause any noticeable effect, but some with particularly complex animations may cause slowdown. To limit the effect that this has on your system, right click on the advert and under the quality heading, select low.<br>
<br>
If there is an advert which is causing particularly bad slow down, even when you set it to low quality, then please report it to us. Click on the Question-Mark "Icon" shown to the bottom right of the advert. This will pop up a window with instructions on how to report that advert.<br>
<br>
To find out more information about Advertising click <a href="../Customer_Support/Advertising/category.html">here</a><br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
