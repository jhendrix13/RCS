<h1>Rule 12 - Real World Item Trading</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
<?php echo $data['wb_name']; ?> items must only be exchanged for other items/services within the game.<br>
<br>
Exchanging <?php echo $data['wb_name']; ?> items for items or other benefits in other online games, real-life money or other real-life benefits is not allowed.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule for three reasons: Firstly because we don&rsquo;t want <?php echo $data['wb_name']; ?> to be just a game in which you can buy your way to success, if we let players start doing this it devalues the game for other players. We feel your status in real-life shouldn&rsquo;t affect your ability to be successful in the game. Secondly we cannot monitor the transactions made between players outside the game, and if players swap items for external benefits we can&rsquo;t see, to us it looks like an unbalanced suspicious trade. Thirdly, often when players attempt to break this rule and sell items for real cash the items are actually stolen, so you could even end up getting in trouble for receiving stolen property. If you purchased an account you might find it suddenly returned to the original rightful owner and yourself out of pocket.<br>
<br>
<h4 style="display: inline;">
Isn&rsquo;t it up to me what I do with my items & account?
</h4><br>
No, the terms and conditions state that your <?php echo $data['wb_name']; ?> character and account and items are and remain the property of Jagex.<br>
<br>
<h4 style="display: inline;">
I have seen an account for sale on Ebay, what should I do?
</h4><br>
<br>
Please rest assured that members of Jagex staff check all known auction sites on a daily basis to ensure that no <?php echo $data['wb_name']; ?> accounts are sold via an online auctioning web site.<br>
<br>
It should be remembered that all accounts (and items) remain the property of Jagex at all times, and we will not hesitate to remove such items from the game if we have evidence that rule breaking has taken place.<br>
<br>
<h4 style="display: inline;">
Am I allowed to accept a forum signature as payment for an item?
</h4><br>
<br>
Forum signatures are not items or services in-game, therefore you are not able to accept these as payment for an item in-game.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
