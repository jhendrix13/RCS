<h1>Rule 8 - Multiple Logging-In</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You may create more than one <?php echo $data['wb_name']; ?> account, but if you do, you may not log in more than one account at any time, and they must not interact with each other in any way. This includes 'drop trading' or any other method of item transfer.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule to maintain a fair gaming experience. We don&rsquo;t want <?php echo $data['wb_name']; ?> to become a game where everyone feels they need to use multiple accounts at once just to compete! It&rsquo;s much better to team up with your friends.<br>
<br>
<h4 style="display: inline;">
Can I use third party software to log into an account for me while I am logging into another (so that I personally am only using one account)?
</h4><br>
<br>
No, you may not use third party software to transfer items - see <a href="http://www.<?php echo $data['wb_name']; ?>.com/Rules_of_Conduct/Rule_7_Using_Third_Party_Software.wsi">Rule 7</a>.<br>
<br>
<h4 style="display: inline;">
I have two accounts. Can my friend play one so I can trade between the characters?
</h4><br>
<br>
No, this goes against this rule. You are not allowed to trade between two characters owned by one player. You would also be in breach of the Account Sharing/Trading rule (see <a href="http://www.<?php echo $data['wb_name']; ?>.com/Rules_of_Conduct/Rule_6_Account_Sharing_Trading.wsi">Rule 6</a>).<br>
<br>
<h4 style="display: inline;">
Can I give items to another player from one of my accounts, so that they can then give the items to another character that I play?
</h4><br>
<br>
No, this goes against the rule. You may not transfer items between your accounts in any way.<br>
<br>
<h4 style="display: inline;">
My brother and I both play from our own accounts in our house. Is it all right if we trade items?
</h4><br>
<br>
Yes, this is not going against the rules of conduct providing you are both playing from two separate accounts created by each of you.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
