<h1>Rule 10 - False Representations</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
You must not misuse <?php echo $data['wb_name']; ?> Customer Support. This includes threatening or reporting an innocent person, supplying false information and abusing Jagex staff.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule so we can help and assist you as quickly as possible. Misuse of customer support prevents us from helping players who genuinely need assistance.<br>
<br>
<h4 style="display: inline;">
What sort of things would be classed as &lsquo;misuse of customer support&rsquo;?
</h4><br>
<br>
Submitting false abuse reports, making false password requests (attempting to steal someone's account), sending offensive messages to Jagex staff, or misuse of the in-game Report Abuse button can lead to Customer Support being withdrawn from your account.<br>
<br>
<h4 style="display: inline;">
I would like some of my levels to be altered. Is this possible?
</h4><br>
<br>
We are afraid that we cannot alter ANY player's stats for ANY reason. We have the firm belief that all skills must be earned through game play and should be representative of the activity undertaken on that account. This is the case regardless of whether a player wishes a certain skill to be raised or lowered.<br>
<br>
<h4 style="display: inline;">
Can I have some free items?
</h4><br>
<br>
Unfortunately, it is not possible for us to give players any items, stats, or quest completions upon request. All items must be earned through game play.<br>
<br>
We understand that it can be upsetting to see other players going around with high levels and top-end equipment when you don't have any, but don't lose heart. Keep training, questing, fighting and playing and you will soon earn valuable items.<br>
<br>
<br>
<h4 style="display: inline;">
Are there any cheats for <?php echo $data['wb_name']; ?>?
</h4><br>
<br>
There are no cheat codes available for <?php echo $data['wb_name']; ?>, nor do we have any intention of adding any to the game. All gains have to be made fairly and via honest game play: any usage of cheats or bugs, or attempting to discover the existence of any cheats or bugs, is a direct violation of the <a href="../Rules_of_Conduct/category.html">Rules of Conduct</a>.<br>
<br>
Please do not send in any more requests for such information as your account will be banned for attempted Cheating and for Misuse of Customer Support if you do so.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
