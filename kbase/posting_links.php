<h1>Rule 11 - Advertising/Website</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 2nd May 2007</span><br>
<br>
You are not allowed to actively advertise in the <?php echo $data['wb_name']; ?> game or <?php echo $data['wb_name']; ?> forums.<br>
<br>
This includes advertising any website or product, and no web addresses are allowed. Telling other players any web-address is not allowed.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
Links to sites can sometimes be dangerous to players (by leading you to sites with inappropriate content) or your computer (by downloading a virus when you go to the site). This precaution has been taken in order that no links to any sites may be advertised in-game or in the <?php echo $data['wb_name']; ?> Forums, thereby protecting you and your computer.<br>
<br>
<h4 style="display: inline;">
Does this include messenger program IDs and IRC channels?
</h4><br>
<br>
Yes. This is not allowed.<br>
<br>
<h4 style="display: inline;">
Does this include file-sharing websites (like video sites)?
</h4><br>
<br>
Yes. This is not allowed.<br>
<br>
<h4 style="display: inline;">
Does this rule include giving out the link to my clan/fan site?
</h4><br>
<br>
Yes. Unfortunately we just don&rsquo;t have the resources to check every website being mentioned, and even well meaning sites could inadvertently contain risks. So this rule covers ALL websites.<br>
<br>
<h4 style="display: inline;">
Can I advertise my website in <?php echo $data['wb_name']; ?>?
</h4><br>
<br>
No, you cannot. As you may know, some sites are used to harvest unsuspecting players' passwords which spoils players enjoyment of the game. We take password security very seriously and therefore have to enforce a rule whereby no websites are advertised in game.<br>
<br>
Clearly this may result in an injustice against players who create websites designed to help the <?php echo $data['wb_name']; ?> Community. However, we need to ensure that player accounts are absolutely secure, and to this end we have developed this policy. We hope that at some point in the future this can change, and more community sites will take the place of hack sites. For the time being though, account security is absolutely essential.<br>
<br>
<h4 style="display: inline;">
Do you have any conditions that you would like Fan Sites to abide by?
</h4><br>
<br>
Yes we do. Please see the below:<br>
<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li style="list-style: none">
<br>
</li>
<li>The site should abide by the Terms of Service for <?php echo $data['wb_name']; ?>.<br>
<br>
</li>
<li>The site should not promote any activity that is against our Rules of Conduct.<br>
<br>
</li>
<li>The site should not publish an article or text taken from the Jagex or <?php echo $data['wb_name']; ?> websites without permission. If you do use any text or images you must acknowledge <?php echo $data['wb_name']; ?>.com as the source, and link back to our site.<br>
<br>
</li>
<li>The site should not link to or promote any adult sites, or sites that conflict with Jagex interests.<br>
<br>
</li>
<li>The site should not promote any sites or information that provide or support hacking, software piracy or other illegal activities.<br>
</li>
</ul><br>
<br>
<h4 style="display: inline;">
Can I promote my Clan website in the game or in the forums?
</h4><br>
<br>
It is a breach of the rules to post anything promoting a website in the forums or in the game. As such, if you do post the URL for your clan site you could be banned.<br>
<br>
We understand this may cause some inconvenience, but please remember that this rule is there to make it impossible for people to send players to scam sites.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
