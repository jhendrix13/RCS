<h1>Rule 2 - Item Scamming</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 28th March 2007</span><br>
<br>
You must not scam or deceive other players. Lying to other players for your own personal gain is not in the spirit of the game.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule to help protect new players, and to help prevent the game being spoilt for others. If you don't understand why we have this rule then try to imagine how you would feel if you had spent hours achieving something and somebody else had then tricked you out of it. We want <?php echo $data['wb_name']; ?> to be fun, and being scammed is anything but fun.<br>
<br>
<h4 style="display: inline;">
What does 'scam' mean?
</h4><br>
<br>
To 'scam' means to cheat another player during a trade. Lying to other players for your own personal gain is not in the spirit of the game and will not be tolerated.<br>
<br>
<h4 style="display: inline;">
Is it a scam to claim an item is rare when it isn't?
</h4><br>
<br>
Yes<br>
<br>
<h4 style="display: inline;">
Is it a scam to switch items at the last second before pressing accept, and hoping the other player won't notice?
</h4><br>
<br>
Yes<br>
<br>
<h4 style="display: inline;">
Is it a scam to ask for lots of money for an item?
</h4><br>
<br>
It is not considered a scam to ask for large amounts of money for an item, but it is not in the spirit of the game to over price an item.<br>
<br>
It is not our intention to set limits on what players trade. It is a player's responsibility to make sure that they are paying a good price for an item.<br>
<br>
We would suggest doing research prior to future trades by using the <?php echo $data['wb_name']; ?>.com Marketplace forum or talking to other players.<br>
<br>
Remember, the second trade-window is there to keep you safe from scammers. If someone is not giving you enough time to check the items being traded then we would strongly advise you to find someone else to trade with. Do not hit the 'Accept' button in the second stage of trading unless you are totally satisfied with the trade. You will not get in trouble for scamming if you choose to take your time and check that everything is as it should be.<br>
<br>
<h4 style="display: inline;">
If someone picks up my arrows is that scamming?
</h4><br>
<br>
No, if someone picks up an item that you have dropped because you no longer require the item then this is not item scamming. When you drop an item it takes a short length of time until it becomes visible to other players, so even if you drop the item accidentally you can still retrieve it again before anyone else has the chance to pick it up.<br>
<br>
You may not drop items with the intention of trading them to another of your characters. This type of drop-trading is against <a href="http://www.<?php echo $data['wb_name']; ?>.com/Rules_of_Conduct/Rule_8_Multiple_Logging_In.wsi">Rule 8</a> in the rules of conduct.<br>
<br>
<h4 style="display: inline;">
Can you trim armour?
</h4><br>
<br>
No, no one can trim armour. Armour that is trimmed has been found in-game as trimmed armour. There is no way a player can trim e.g. Asking for Rune armour to make it trimmed.<br>
<br>
<h4 style="display: inline;">
What are some examples of item scams?
</h4><br>
<br>
Some examples of item scams are:<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li style="list-style: none">
<br>
</li>
<li>Offering an item and then removing the item within the first or second trade screen.<br>
</li>
<li>Offering an item and changing the agreed price within the first or second trade screen.<br>
</li>
<li>Misleading players by providing false or inaccurate information, for your own personal gain.<br>
</li>
<li>Luring players into the Wilderness under false pretences.<br>
</li>
</ul><br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
