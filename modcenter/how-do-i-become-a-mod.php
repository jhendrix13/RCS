<h2 align="center">How do I become a moderator?</h2>
<br />
We keep a look-in on all potential moderators, no requests are taken and no friends are promoted. Everyone who is promoted has been hand picked by staff; primarily the Administration.<br />
<br />
We take into account several factors when considering a moderator. We check their reports logged, their behaviour on the forums, the messages sent to us regarding them and their general forums and game etiquette.<br /><br />

To popular belief I think it's due I incorporate this into here: If you wish to be a moderator and you have received some form of punishment in the past, this does not mean your chances are over. We take into account that people mature over time. We look at the date of the previous offence and the seriousness of it. If we believe you have matured enough you will have as much of a chance as someone else would.<br />
<br />
We do not give out details on how close someone is getting to becoming a Moderator but rest assured that if you are doing what is needed to become a Moderator then we will take notice. If you do get selected to be a Moderator, a message will be sent to your account. Once you have accepted and the confirmation has been received, you will get your silver/green crown and Moderator abilities.
